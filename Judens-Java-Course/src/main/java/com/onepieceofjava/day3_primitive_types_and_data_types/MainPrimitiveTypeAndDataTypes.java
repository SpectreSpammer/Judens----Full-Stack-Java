package com.onepieceofjava.day3_primitive_types_and_data_types;

public class MainPrimitiveTypeAndDataTypes {
    public static void main(String[] args) {

        //8 bit
        byte age = 29;
        System.out.println("byte: " + age);

        //16 bit
        short departmentCode = 12345;
        System.out.println("short: " + departmentCode);


        //32 bit
        int id = 1234567890;
        System.out.println("int: " + id);

        float hourlyRate = 1000.75F;
        System.out.println("float: " + hourlyRate);

        //64 bit
        double salary = 60000.75f;
        System.out.println("double: " + salary);

        long uuid = 1234567890987654321L;
        System.out.println("long: " + uuid);


        //boolean
        boolean isFte = false;
        System.out.println("boolean: " + isFte);

        //string
        String name = "judens";
        System.out.println("String: " + name);
    }
}
